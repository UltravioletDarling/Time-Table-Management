     private void lecturecombo(){
     
         try {
             String sql = "SELECT * FROM lecturers";
             pst = con.prepareStatement(sql);
             rs = pst.executeQuery();
             
             while(rs.next()){
             String lecname = rs.getString("lecturers_name");
             
             LecnameCombo.addItem(lecname);
             Lecname2Combo.addItem(lecname);
             }
             
         } catch (Exception e) {
             
         }
     
     }
