    private void btn_signupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_signupActionPerformed
        String username = sname.getText();
        String role = rolecombo.getSelectedItem().toString();
        String email = semail.getText();
        String password = spassword.getText();

        Connection con;
        PreparedStatement insert;

        try {

            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/timetable","root","");
            insert = con.prepareStatement("insert into login(username,role,email,password)values(?,?,?,?)");

            insert.setString(1, username);
            insert.setString(2, role);
            insert.setString(3, email);
            insert.setString(4, password);

            insert.executeUpdate();

            JOptionPane.showMessageDialog(this,"Successfully Registered");

        } catch (ClassNotFoundException ex) {

            java.util.logging.Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        catch (SQLException ex) {

            java.util.logging.Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_signupActionPerformed

