    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        // TODO add your handling code here:
        Connection con;
        PreparedStatement pst;

        try {

            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/timetable","root","");
            pst = con.prepareStatement("SELECT * FROM login WHERE username=? AND password=?");

            pst.setString(1, luser.getText());
            pst.setString(2, lpassword.getText());
            ResultSet rs = pst.executeQuery();

            if(rs.next()){
                JOptionPane.showMessageDialog(null, "Login Successful!");
                dispose();
                new MainDashboard().setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null, "Incorrect Credentials");
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);

        } catch (ClassNotFoundException ex) {
            java.util.logging.Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }//GEN-LAST:event_btn_loginActionPerformed

