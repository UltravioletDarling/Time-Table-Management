    private void GenerateLocationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GenerateLocationActionPerformed
      int p;
              try {
            
            String location = locationcombo.getSelectedItem().toString();
        
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/timetable","root","");
            pst = con.prepareStatement("select * from locationactivesessions where location='"+ location +"' order by timeslot" );
           
           
            ResultSet rs = pst.executeQuery();
            ResultSetMetaData Rss = rs.getMetaData();
            p = Rss.getColumnCount();
            
            DefaultTableModel dft = (DefaultTableModel) timetablelocation.getModel();
            dft.setRowCount(0);
            
               while(rs.next())
               {
                   Vector v2 = new Vector();
                   
                   for(int q=1; q<=p; q++)
                   {
                       v2.add(rs.getString("monday"));
                       v2.add(rs.getString("tuesday"));
                       v2.add(rs.getString("wednesday"));
                       v2.add(rs.getString("thursday"));
                       v2.add(rs.getString("friday"));
                      
                       
                   }
                   
                   dft.addRow(v2);
               }
               
            
        } catch (ClassNotFoundException ex) {
            
            Logger.getLogger(Timetablegeneration.class.getName()).log(Level.SEVERE, null, ex);
        }
        catch (SQLException ex) {
            
            Logger.getLogger(Timetablegeneration.class.getName()).log(Level.SEVERE, null, ex);
        }
              
    }//GEN-LAST:event_GenerateLocationActionPerformed

